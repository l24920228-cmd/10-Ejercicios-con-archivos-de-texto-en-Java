    private void btnBuscarMouseClicked(java.awt.event.MouseEvent evt) {                                     

    int id = Integer.parseInt(txtId.getText());

    Producto p = archivo.buscar(id);

    if (p != null) {

        txtNombre.setText(p.getNombre());
        txtDescripcion.setText(p.getDescripcion());
        txtExistencia.setText(String.valueOf(p.getExistencia()));

        for (int i = 0; i < tblDatos.getRowCount(); i++) {

            int idTabla = Integer.parseInt(
                tblDatos.getValueAt(i, 0).toString()
            );

            if (idTabla == id) {

                tblDatos.setRowSelectionInterval(i, i);
                break;
            }
        }

        btnModificar.setEnabled(true);
        btnEliminar.setEnabled(true);
        btnAgregar.setEnabled(false);

    } else {

        JOptionPane.showMessageDialog(this,
            "No encontrado");
    }
}                                      

